  <h3>Précepte 1 : Ne vous intéressez qu’aux conférences et journaux réputés</h3>
  <i><p>“Ceux qui ne savent pas où ils vont sont surpris d’arriver ailleurs.” Pierre Dac</p></i>
  <hr>

  <p style="text-align: justify;">La recherche, le métier que vous avez délibérément
choisi, est un métier essentiellement élitiste et de
vocation. Ce dernier vient du fait qu’il n’existe très
souvent aucune contrainte formelle de rentabilité imposée au chercheur. L’organisme publique qui vous
recrute compte sur votre bonne volonté. Il attend
de vous que vous participiez au rayonnement de la
recherche nationale.</p>

<p>Plusieurs métriques existent pour mesurer le
niveau de rayonnement d’un chercheur. La métrique
la plus utilisée est l’indice de Hirsch (notée h-index),
du nom de son inventeur. Le h-index indique
l’importance de la citation de vos publications par
d’autres chercheurs. D’après Hirsch: "Un scientifique
a un indice h si h de ses Np articles ont chacun au
moins h citations, et les autres (Np-h) articles ont au
plus h citations chacun." (extrait de Wikipédia). Bien
que cette métrique indique le niveau de rayonnement
d’un chercheur, il ne permet pas de savoir si ce rayonnement se situe dans la sphère des chercheurs réputés.
(Une définition de la notion de chercheur réputé est
donnée ci-dessous). Le rayonnement auprès de ces derniers est le rayonnement productif, c’est à dire
celui qui indique l’influence des travaux du chercheur
sur l’avenir. Il est beaucoup plus facile d’atteindre
un rayonnement non productif compte tenu du fait
que la proportion des chercheurs moins réputés est
largement plus importante que la proportion des
chercheurs réputés. Dans mes débuts, j’ai longtemps
cité et considéré un chercheur dont je ne divulguerai
pas l’identité comme une référence dans le domaine
du Cloud Computing. Celui-ci possède un h-index
de 120 à l’heure où j’écris ce livre, sachant qu’au delà d’environ 20 vous êtes considérés comme un
chercheur réputé par ceux qui utilisent cette métrique.
Ce chercheur est considéré par la grande majorité
des chercheurs de mon domaine comme une sommité. Or, depuis que je me suis intéressé aux conférences réputées (voir ci-dessous), je me suis rendu
compte que la très grande majorité des travaux de
ce chercheur n’auraient aucun impact sur l’avenir.
C’est la raison pour laquelle, le h-index ne représente
pas à mon sens une métrique valable. Plusieurs scientifiques ont d’ailleurs démontrer comment améliorer artificiellement son h-index.</p>

<p>Ce qui compte est donc le rayonnement productif,
tel que défini ci-dessus. Par conséquence, ne publiez
vos travaux que dans des conférences ou journaux
fréquentés par les chercheurs réputés de votre domaine. Ces conférences et journaux sont connus. En
effet, les conférences et journaux sont classés selon
leur niveau de réputation. Parmi les classements existants, le plus utilisé par les chercheurs réputés est
le classement australien core ranking1
. Ce dernier
utilise les notations <b> A*, A, B et C </b> pour classer les
conférences et journaux des plus réputés aux moins
réputés.</p>

  </div>

  <div class="item">

  <p>La réputation d’une conférence est caractérisée par
la réputation des membres du comité de relecture
des articles. L’on pourrait penser à une situation de
cul de sac: pour être considéré comme un chercheur
réputé il faut en quelque sorte avoir l’approbation
des chercheurs réputés. D’où ces derniers tirentils leur légitimité? Cette légitimité provienne des pionniers de chaque domaine de recherche. En effet, les pionniers (légitimes à leur époque) ont été à
l’origine des premières conférences réputées, ainsi
que de la constitution de leur comité de relecture. Les
chercheurs approuvés par ces pionniers, ainsi que
ceux formés par ces derniers ont permis la perpétuation du mode de sélection rigoureux des conférences
et journaux réputés. A titre d’exemple, le professeur
Frans Kaashoek du MIT, encadré en thèse par le professeur Andy Tanenbaum de l’université de Vrije et
pionnier des systèmes d’exploitation, participe à la constitution de nombreux comités de relecture de
plusieurs conférences et journaux classés <b>A* et A</b> 
du core ranking. Ces chercheurs réputés participent
rarement au comité de relecture des conférences et
journaux de rang inférieur à A, sauf s’il s’agit de
conférences ou journaux en cours de création par
un groupe de chercheurs réputés. Notons que le core
ranking met à jour son classement tous les ans afin de
prendre en compte l’évolution du niveau d’exigence
des conférences et journaux.</p>

<p>Le point étant fait sur la pertinence du classements des conférences et journaux, l’élitisme qu’exige votre
métier de chercheur consiste à ne s’intéresser qu’aux
productions de rang A* et A. Les chercheurs réputés
adoptent essentiellement cette attitude. Ce n’est
qu’en vous confrontant aux productions réputés que
vous serez en mesure d’en produire des travaux de
ce calibre. Vous ne devez penser vos travaux qu’en
fonction de leur capacité à aboutir à une publication
dans une conférence ou journal de rang au moins <b>A</b>,
bien que difficile d’accès. Le reject d’une soumission
ne doit pas être un motif de découragement. Ceci
est le lot commun de tous les chercheurs, y compris
les chercheurs réputés dont le nombre de important
de doctorants masque les échecs vu de l’extérieur.
Les conférences et journaux de rangs <b>B et C</b>  ou non
classés ne doivent pas faire partie de vos objectif. Ils
représentent une perte d’énergie.</p>

<p>En résumé, vous devez être au courant des conférences et journaux réputés de votre domaine, des
chercheurs nationaux et internationaux réputés. Il en
est de même pour vos doctorants qui doivent partager
le précepte présenté dans ce chapitre. Il est de votre responsabilité de le leur inculquer cela.</p>

