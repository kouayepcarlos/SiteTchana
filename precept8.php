  <h3>Précepte 8 : Votre équipe
forme un grande
famille </h3>
  <i><p>“Tout seul on va plus vite, ensemble, on va plus loin.”
  Adage africain</p></i>
  <hr>

  <p>Le métier de chercheur est un métier qui nécessite
à la fois des qualités scientifiques exceptionnelles,
mais également de gestion de groupe. Cette seconde
qualité est aussi importante que la première car c’est
elle qui vous permet de mettre vos doctorants en mouvement. Ce chapitre présente quelques éléments pour
vous permettre de fédérer votre groupe de recherche
derrière vous.</p>

<p>Chaque doctorant a besoin de considération. Vous
devez montrer à chaque doctorant qu’il est important,
qu’il possède au moins une qualité exceptionnelle
que les autres ne possèdent pas. Le doctorant doit
le savoir. Le lui faire savoir si possible en présence
des autres. Ne pas avoir ouvertement une préférence
pour un doctorant. Mettre tour à tour chaque doctorant à l’honneur. Faire savoir au doctorant qu’il
compte pour vous, que votre réussite est liée à la sienne. Le doctorant doit avoir le sentiment que vous
faites partie du même bateau.</p>

<p>Créer un esprit famille au sein de votre équipe.
Pour cela, entreprendre plusieurs actions qui vont
dans ce sens, voici quelques exemples. Vous intéresser à la vie du doctorant hors recherche, sans être
intrusif. Connaître ses centres d’intérêt, ses loisirs
et vous y intéresser. Pour ce qui me concerne par
exemple, j’essaie de connaître la date d’anniversaire
de mes doctorants. De cette façon, je lui laisse un
message le jour venu, ou bien j’incite ses collègues à
lui organiser un petit événement surprise. Organisez
des repas et autres activités d’équipes. Vous devez
être au courant de l’étant de santé de vos doctorants.
En cas de maladie, d’un doctorant, n’hésitez pas à
lui rendre visite et d’inciter les autres à le faire. Par
exemple, j’ai été emmener à annuler un déplacement
de présentation d’un article à l’étranger pour prendre m’occuper de la situation critique d’un de mes
doctorants, ceci malgré le risque de non publication
de l’article. Ne pas fliquer les doctorants, ce qui ne
signifie pas que vous ne devez pas imposer une certaine présence au laboratoire. C’est cette présence
qui permet de créer l’esprit de groupe et de famille.</p>

